# =============================================================================

def plot_confusion_matrices(df, save_path="fig1_confusion_matrices.pdf"):
    y_true = df["true_label"]

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))
    fig.suptitle(
        "Confusion Matrices — VADER vs FinBERT on WallStreetBets Dataset",
        fontsize=13, fontweight="bold"
    )

    models = [
        ("VADER",   "vader_pred",   "Blues"),
        ("FinBERT", "finbert_pred", "Oranges"),
    ]

    for ax, (name, col, cmap) in zip(axes, models):
        y_pred  = df[col]
        cm      = confusion_matrix(y_true, y_pred, labels=LABEL_ORDER)
        cm_norm = cm.astype(float) / cm.sum(axis=1, keepdims=True)

        sns.heatmap(
            cm_norm,
            annot=True, fmt=".2f", cmap=cmap,
            xticklabels=LABEL_NAMES, yticklabels=LABEL_NAMES,
            linewidths=0.5, linecolor="white",
            vmin=0, vmax=1, ax=ax,
            cbar_kws={"label": "Row-normalised proportion", "shrink": 0.8}
        )

        # Overlay raw counts
        for i in range(len(LABEL_ORDER)):
            for j in range(len(LABEL_ORDER)):
                ax.text(j + 0.5, i + 0.72, f"n={cm[i,j]}",
                        ha="center", va="center", fontsize=8, color="dimgray")

        acc = accuracy_score(y_true, y_pred)
        f1  = f1_score(y_true, y_pred, average="macro")
        ax.set_title(f"{name}  |  Acc={acc:.3f}  |  Macro F1={f1:.3f}",
                     fontsize=11, pad=10)
        ax.set_xlabel("Predicted Label", fontsize=10)
        ax.set_ylabel("True Label", fontsize=10)

    plt.tight_layout()
    plt.savefig(save_path, bbox_inches="tight", dpi=300)
    print(f"[✓] Saved: {save_path}")
    plt.show()


