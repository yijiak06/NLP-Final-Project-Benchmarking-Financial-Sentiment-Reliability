def plot_counterfactual_consistency(df, save_path="fig2_counterfactual.pdf"):
    """
    Panel A: FinBERT label transition heatmap (original → counterfactual).
             These are the 51 true logic failures — cases where the text
             actually changed but FinBERT still didn't flip its label.
    Panel B: VADER compound score delta scatter, coloured by FinBERT stability.
    """
    label_map = {-1: "Negative", 0: "Neutral", 1: "Positive"}
    cat       = ["Negative", "Neutral", "Positive"]

    df = df.copy()
    df["fin_orig_label"] = df["fin_orig"].map(label_map)
    df["fin_cf_label"]   = df["fin_cf"].map(label_map)
    df["vader_delta"]    = df["vader_cf"] - df["vader_orig"]
    df["finbert_stable"] = (df["fin_orig"] == df["fin_cf"]).map(
        {True: "Stable", False: "Flipped"}
    )

    transitions = (
        df.groupby(["fin_orig_label", "fin_cf_label"])
        .size()
        .unstack(fill_value=0)
        .reindex(index=cat, columns=cat, fill_value=0)
    )

    n_flips  = (df["fin_orig"] != df["fin_cf"]).sum()
    n_total  = len(df)
    flip_pct = 100 * n_flips / n_total

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))
    fig.suptitle(
        "Counterfactual Consistency on TSLA Dataset  "
        "(keyword swap e.g. 'bullish' → 'bearish')",
        fontsize=13, fontweight="bold"
    )

    # Panel A — FinBERT transition heatmap
    ax = axes[0]
    sns.heatmap(
        transitions, annot=True, fmt="d", cmap="YlOrRd",
        linewidths=0.5, linecolor="white", ax=ax,
        cbar_kws={"label": "Count", "shrink": 0.8}
    )
    ax.set_title("FinBERT: Label Transitions (Original → Counterfactual)", fontsize=11)
    ax.set_xlabel("Counterfactual Prediction", fontsize=10)
    ax.set_ylabel("Original Prediction", fontsize=10)
    ax.set_xticklabels(ax.get_xticklabels(), rotation=30, ha="right")
    ax.text(
        0.97, 0.03,
        f"{n_flips}/{n_total} texts ({flip_pct:.0f}%) caused a label flip",
        transform=ax.transAxes, ha="right", va="bottom", fontsize=9,
        color="firebrick",
        bbox=dict(boxstyle="round,pad=0.3", facecolor="lightyellow", alpha=0.85)
    )

    # Panel B — VADER delta scatter
    ax = axes[1]
    colors = {"Flipped": "#e74c3c", "Stable": "#3498db"}
    for group, gdf in df.groupby("finbert_stable"):
        ax.scatter(
            gdf["vader_orig"], gdf["vader_delta"],
            label=group, color=colors[group],
            alpha=0.75, edgecolors="white", s=70, linewidths=0.5
        )

    ax.axhline(0, color="gray", linestyle="--", linewidth=0.8)
    ax.set_xlabel("VADER Score — Original Text", fontsize=10)
    ax.set_ylabel("VADER Score Delta  (Counterfactual − Original)", fontsize=10)
    ax.set_title(
        "VADER Sensitivity to Counterfactual Rewrite\n"
        "(coloured by whether FinBERT label flipped)",
        fontsize=11
    )
    ax.legend(title="FinBERT Label", frameon=False)

    plt.tight_layout()
    plt.savefig(save_path, bbox_inches="tight", dpi=300)
    print(f"[✓] Saved: {save_path}")
    plt.show()


